def ui(fw, fh):
    """
    Fully dynamic UI sizing.
    Uses the DIAGONAL of the frame as the base metric — this gives
    balanced sizing for both portrait and landscape orientations.
    A 1920x1080 landscape and a 1080x1920 portrait both have the same
    diagonal (~2203px) so text/lines look identical on both.
    """
    diag = math.hypot(fw, fh)          # diagonal in pixels
    # Reference diagonal for a 1280x720 frame ≈ 1469px
    # All constants are tuned for that reference and scale linearly.
    REF  = 1469.0
    s    = diag / REF                  # scale factor

    # Font sizes — clamp so they never go below readable minimum
    fs_large  = max(0.45, 0.65 * s)
    fs_medium = max(0.38, 0.52 * s)
    fs_small  = max(0.30, 0.40 * s)

    # Line widths
    lw_thick = max(1, int(2.0 * s))
    lw_thin  = max(1, int(1.2 * s))

    # UI element heights — scale with diagonal
    hud_h   = max(40,  int(52  * s))
    alert_h = max(28,  int(36  * s))
    pad     = max(5,   int(8   * s))
    dot_r   = max(4,   int(6   * s))

    return {
        "md":         min(fw, fh),
        "diag":       diag,
        "s":          s,
        "fs_large":   fs_large,
        "fs_medium":  fs_medium,
        "fs_small":   fs_small,
        "lw_thick":   lw_thick,
        "lw_thin":    lw_thin,
        "hud_h":      hud_h,
        "alert_h":    alert_h,
        "pad":        pad,
        "dot_r":      dot_r,
        "zone_alpha": 0.30,
    }

