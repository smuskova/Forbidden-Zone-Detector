# ─────────────────────────────────────────────────────────────────────────────
#  KALMAN BOX TRACKER  (constant-velocity, same model as SORT/DeepSORT)
#  State:       [cx, cy, w, h, vx, vy, vw, vh]
#  Measurement: [cx, cy, w, h]
# ─────────────────────────────────────────────────────────────────────────────
class KalmanBoxTracker:
    count = 0

    def __init__(self, box):
        self.id       = KalmanBoxTracker.count
        KalmanBoxTracker.count += 1
        self.hits     = 1
        self.no_match = 0

        self.kf_x = np.zeros((8, 1), dtype=np.float32)
        cx = box[0] + box[2] / 2
        cy = box[1] + box[3] / 2
        self.kf_x[:4, 0] = [cx, cy, float(box[2]), float(box[3])]

        self.kf_F = np.eye(8, dtype=np.float32)
        for i in range(4):
            self.kf_F[i, i + 4] = 1.0

        self.kf_H = np.zeros((4, 8), dtype=np.float32)
        for i in range(4):
            self.kf_H[i, i] = 1.0

        self.kf_P = np.eye(8, dtype=np.float32) * 10.0
        self.kf_P[4:, 4:] *= 1000.0
        self.kf_Q = np.eye(8, dtype=np.float32)
        self.kf_Q[4:, 4:] *= 0.01
        self.kf_R = np.eye(4, dtype=np.float32) * 1.0

    def predict(self):
        self.kf_x = self.kf_F @ self.kf_x
        self.kf_P = self.kf_F @ self.kf_P @ self.kf_F.T + self.kf_Q
        return self._to_box()

    def update(self, box):
        cx = box[0] + box[2] / 2
        cy = box[1] + box[3] / 2
        z  = np.array([[cx], [cy], [float(box[2])], [float(box[3])]], dtype=np.float32)
        S  = self.kf_H @ self.kf_P @ self.kf_H.T + self.kf_R
        K  = self.kf_P @ self.kf_H.T @ np.linalg.inv(S)
        self.kf_x  = self.kf_x + K @ (z - self.kf_H @ self.kf_x)
        self.kf_P  = (np.eye(8, dtype=np.float32) - K @ self.kf_H) @ self.kf_P
        self.hits     += 1
        self.no_match  = 0

    def _to_box(self):
        cx, cy, w, h = self.kf_x[:4, 0]
        return (int(cx - w / 2), int(cy - h / 2), int(w), int(h))

    def get_box(self):
        return self._to_box()

# ─────────────────────────────────────────────────────────────────────────────
#  CENTROID TRACKER
# ─────────────────────────────────────────────────────────────────────────────
class CentroidTracker:
    def __init__(self, max_disappeared=1, max_distance=80):
        self.next_id     = 0
        self.objects     = {}
        self.boxes       = {}
        self.disappeared = defaultdict(int)
        self.max_dis     = max_disappeared
        self.max_dist    = max_distance

    def _c(self, b):
        return (int(b[0] + b[2] / 2), int(b[1] + b[3] / 2))

    def update(self, boxes):
        if not boxes:
            for oid in list(self.disappeared):
                self.disappeared[oid] += 1
                if self.disappeared[oid] > self.max_dis:
                    del self.objects[oid]
                    del self.boxes[oid]
                    del self.disappeared[oid]
            return dict(self.objects), dict(self.boxes)

        nc = [self._c(b) for b in boxes]
        if not self.objects:
            for c, b in zip(nc, boxes):
                self.objects[self.next_id] = c
                self.boxes[self.next_id]   = b
                self.disappeared[self.next_id] = 0
                self.next_id += 1
        else:
            ids = list(self.objects.keys())
            oc  = list(self.objects.values())
            D   = np.array([[math.hypot(a[0]-b[0], a[1]-b[1])
                             for b in nc] for a in oc])
            rows = D.min(axis=1).argsort()
            cols = D.argmin(axis=1)[rows]
            ur, uc = set(), set()
            for r, c in zip(rows, cols):
                if r in ur or c in uc or D[r, c] > self.max_dist:
                    continue
                oid = ids[r]
                self.objects[oid] = nc[c]
                self.boxes[oid]   = boxes[c]
                self.disappeared[oid] = 0
                ur.add(r); uc.add(c)
            for r in set(range(len(ids))) - ur:
                oid = ids[r]
                self.disappeared[oid] += 1
                if self.disappeared[oid] > self.max_dis:
                    del self.objects[oid]
                    del self.boxes[oid]
                    del self.disappeared[oid]
            for c in set(range(len(nc))) - uc:
                self.objects[self.next_id] = nc[c]
                self.boxes[self.next_id]   = boxes[c]
                self.disappeared[self.next_id] = 0
                self.next_id += 1
        return dict(self.objects), dict(self.boxes)

