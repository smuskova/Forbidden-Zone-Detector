def run(
    input_path   = None,
    output_path  = None,
    model_name   = "yolov8n.pt",
    zone_mode    = "custom",
    n_zones      = 1,
    conf_thresh  = 0.40,
    detect_scale = DETECT_SCALE,
    detect_every = DETECT_EVERY,
    csv_log      = True,
    save_video   = True,
):
    # 1. File picker
    if not input_path:
        print("\n[SETUP] Select your video file...")
        input_path = pick_video_file()
    if not input_path or not os.path.isfile(input_path):
        print("[ERROR] No valid video selected.")
        sys.exit(1)

    # 2. Output path (optional)
    if save_video and output_path is None:
        auto_out    = os.path.splitext(input_path)[0] + "_detected.mp4"
        output_path = pick_output_file(auto_out)
        # If user cancelled save dialog -> no output file
    csv_path = (os.path.splitext(input_path)[0] + "_violations.csv"
                if csv_log else None)

    print(f"\n[INFO] Input        : {input_path}")
    print(f"[INFO] Output       : {output_path or 'not saving'}")
    print(f"[INFO] Detect every : {detect_every} frames (Kalman fills the rest)")
    print(f"[INFO] Detect scale : {detect_scale}")

    # 3. Probe video
    cap = cv2.VideoCapture(input_path)
    if not cap.isOpened():
        print(f"[ERROR] Cannot open {input_path}")
        sys.exit(1)
    orig_w = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    orig_h = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
    fps    = cap.get(cv2.CAP_PROP_FPS) or 24.0
    ret, first = cap.read()
    cap.release()
    if not ret:
        print("[ERROR] Cannot read first frame.")
        sys.exit(1)

    dw = int(orig_w * detect_scale)
    dh = int(orig_h * detect_scale)
    print(f"[INFO] Original     : {orig_w}x{orig_h}  "
          f"({'portrait' if orig_h > orig_w else 'landscape'})")
    print(f"[INFO] Process size : {dw}x{dh}   FPS: {fps:.2f}")

    # 4. Zone setup — always fresh, clean preview frame
    cv2.destroyAllWindows(); cv2.waitKey(1)
    preview              = cv2.resize(first, (dw, dh)).copy()
    zones, zone_types    = setup_zones(preview, zone_mode, n_zones, dw, dh)
    masks                = [build_mask(dh, dw, z) for z in zones]
    cv2.destroyAllWindows(); cv2.waitKey(1)

    print(f"\n[INFO] {len(zones)} forbidden zone(s) active.")
    print("[INFO] Starting live analysis... (Q=quit  SPACE=pause)\n")

    # 5. Launch producer thread
    frame_q    = queue.Queue(maxsize=QUEUE_SIZE)
    stop_event = threading.Event()

    stats_out = {}   # filled by producer, used for report after loop

    producer = threading.Thread(
        target=_producer,
        args=(input_path, output_path, model_name,
              zones, masks, zone_types, conf_thresh,
              detect_scale, detect_every,
              csv_path, frame_q, stop_event, stats_out),
        daemon=True)
    producer.start()

    # 6. Display loop (main thread) — reads at native FPS
    win            = "Forbidden Zone Detector  |  Q=quit  SPACE=pause"
    paused         = False
    frame          = None
    window_created = False

    while True:
        if not paused:
            try:
                tag, data, vid_fps = frame_q.get(timeout=5.0)
            except queue.Empty:
                break

            if tag == "done":
                break
            elif tag == "error":
                print(f"\n[ERROR] {data}")
                break
            elif tag == "frame":
                frame = data

        if frame is not None:
            # Create/resize window only when we have the first real frame
            fh_f, fw_f = frame.shape[:2]
            if not window_created:
                cv2.namedWindow(win, cv2.WINDOW_NORMAL)
                _fit_window(win, fw_f, fh_f)
                window_created = True
            cv2.imshow(win, frame)

        # Native FPS delay
        delay = max(1, int(1000.0 / fps))
        k = cv2.waitKey(delay) & 0xFF
        if k in (ord('q'), 27):
            print("[INFO] Stopped by user.")
            stop_event.set()
            break
        elif k == ord(' '):
            paused = not paused
            print("[INFO] " + ("Paused." if paused else "Resumed."))

    stop_event.set()
    producer.join(timeout=3)
    cv2.destroyAllWindows()

    print(f"\n[INFO] Analysis finished.")
    if output_path and os.path.isfile(output_path):
        print(f"[INFO] Saved: {output_path}")
    if csv_path and os.path.isfile(csv_path):
        print(f"[INFO] CSV:   {csv_path}")

    # Generate heatmap PNG + HTML report
    base_name    = os.path.splitext(input_path)[0]
    heatmap_path = base_name + "_heatmap.png"
    report_path  = base_name + "_report.html"

    heatmap_obj     = stats_out.get("heatmap_obj")
    first_frame_img = stats_out.get("first_frame_img")

    if heatmap_obj is not None and first_frame_img is not None:
        heatmap_obj.save_png(heatmap_path, first_frame_img)

    stats_out["video_name"]   = os.path.basename(input_path)
    stats_out["generated_at"] = datetime.now().strftime("%Y-%m-%d %H:%M:%S")

    try:
        generate_html_report(report_path, heatmap_path, stats_out)
        import webbrowser
        webbrowser.open(report_path)
        print(f"[INFO] Report opened in browser: {report_path}")
    except Exception as e:
        print(f"[WARN] Could not generate report: {e}")

