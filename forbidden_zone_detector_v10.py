RISK_LOW    = 30
RISK_MEDIUM = 70

def compute_risk_score(zone_type, dwell_frames, fps,
                       speed_px_per_frame, nearby_count,
                       predicted_in_s):
    """
    Compute a 0-100+ danger score for one tracked person.
    Returns (score, label, colour)
    """
    score = 0

    # Current zone
    if zone_type == "yellow":
        score += 20
    elif zone_type == "red":
        score += 50

    # Dwell time in zone
    dwell_s = dwell_frames / fps if fps > 0 else 0
    if dwell_s > 5:
        score += 15
    elif dwell_s > 2:
        score += 8

    # Speed (fast movement = higher risk)
    if speed_px_per_frame > 8:
        score += 25
    elif speed_px_per_frame > 4:
        score += 12

    # Group density
    if nearby_count >= 4:
        score += 15
    elif nearby_count >= 2:
        score += 7

    # Predictive — will enter red zone soon
    if predicted_in_s is not None:
        if predicted_in_s < 1.5:
            score += 45
        elif predicted_in_s < 3.0:
            score += 30

    score = min(score, 100)

    if score >= RISK_MEDIUM:
        return score, "HIGH",   (0, 30, 220)    # red
    elif score >= RISK_LOW:
        return score, "MEDIUM", (0, 140, 255)   # orange
    else:
        return score, "LOW",    (50, 200, 50)   # green


def predict_zone_entry(kf_tracker, masks, zone_types, fps,
                       horizon_s=3.0):
    """
    Extrapolate Kalman state N frames ahead.
    Returns seconds until predicted RED zone entry, or None.
    """
    import copy, numpy as np
    horizon_f = int(fps * horizon_s)
    # Simulate Kalman forward without modifying the real tracker
    x = kf_tracker.kf_x.copy()
    F = kf_tracker.kf_F
    for step in range(1, horizon_f + 1):
        x = F @ x
        cx = int(x[0, 0] - x[2, 0] / 2 + x[2, 0] / 2)   # centre x
        cy = int(x[1, 0] + x[3, 0] / 2)                   # foot y
        cx = int(x[0, 0])
        cy = int(x[1, 0] + x[3, 0] / 2)
        for zi, mask in enumerate(masks):
            ztype = zone_types[zi] if zi < len(zone_types) else "red"
            if ztype != "red":
                continue
            cx_c = min(max(cx, 0), mask.shape[1] - 1)
            cy_c = min(max(cy, 0), mask.shape[0] - 1)
            if mask[cy_c, cx_c] > 0:
                return step / fps   # seconds until entry
    return None


