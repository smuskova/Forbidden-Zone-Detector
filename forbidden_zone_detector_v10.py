def auto_zone(w, h, n=1):
    if n == 1:
        return [[(0, int(h*.35)), (w, int(h*.35)),
                 (w, int(h*.75)), (0, int(h*.75))]]
    y0a, y1a = int(h*.28), int(h*.50)
    y0b, y1b = int(h*.52), int(h*.74)
    return [[(0,y0a),(w,y0a),(w,y1a),(0,y1a)],
            [(0,y0b),(w,y0b),(w,y1b),(0,y1b)]]

def build_mask(h, w, poly):
    m = np.zeros((h, w), dtype=np.uint8)
    cv2.fillPoly(m, [np.array(poly, dtype=np.int32)], 255)
    return m

def feet_in_zone(box, mask):
    """
    Multi-point zone check — tests 5 points across the lower half of the
    bounding box. If ANY point is inside the zone mask the person counts
    as being in the zone. This prevents misses when a large foreground
    person's centre-bottom falls just outside the zone boundary.

    Points tested:
      - bottom-centre  (feet)
      - bottom-left    (25% from left)
      - bottom-right   (75% from left)
      - mid-centre     (at 75% height)
      - mid-left/right (at 75% height, 25% and 75% width)
    """
    x, y, w, h = box
    mh, mw = mask.shape[:2]

    def _in(px, py):
        px = min(max(int(px), 0), mw - 1)
        py = min(max(int(py), 0), mh - 1)
        return mask[py, px] > 0

    # Bottom row (feet level)
    if _in(x + w * 0.50, y + h):        return True  # centre-bottom
    if _in(x + w * 0.25, y + h):        return True  # left-bottom
    if _in(x + w * 0.75, y + h):        return True  # right-bottom
    # Mid-lower body (75% height)
    if _in(x + w * 0.50, y + h * 0.75): return True  # centre-mid
    if _in(x + w * 0.25, y + h * 0.75): return True  # left-mid
    if _in(x + w * 0.75, y + h * 0.75): return True  # right-mid
    return False


# ─────────────────────────────────────────────────────────────────────────────
#  HEATMAP ACCUMULATOR
# ─────────────────────────────────────────────────────────────────────────────
class Heatmap:
    """Accumulates foot-point positions and renders a colourmap overlay."""
    def __init__(self, h, w):
        self.acc = np.zeros((h, w), dtype=np.float32)
        self.h   = h
        self.w   = w

    def add(self, boxes):
        """Add foot centre of each box to the accumulator."""
        for x, y, bw, bh in boxes:
            cx = min(max(int(x + bw / 2), 0), self.w - 1)
            cy = min(max(int(y + bh),     0), self.h - 1)
            # Gaussian splat around foot point
            r = max(self.h, self.w) // 40
            x0, x1 = max(0, cx-r), min(self.w, cx+r)
            y0, y1 = max(0, cy-r), min(self.h, cy+r)
            for iy in range(y0, y1):
                for ix in range(x0, x1):
                    d = math.hypot(ix-cx, iy-cy)
                    if d < r:
                        self.acc[iy, ix] += 1.0 - d/r

    def render(self, base_frame):
        """Return base_frame with heatmap blended on top."""
        if self.acc.max() == 0:
            return base_frame.copy()
        norm = (self.acc / self.acc.max() * 255).astype(np.uint8)
        coloured = cv2.applyColorMap(norm, cv2.COLORMAP_JET)
        mask     = (norm > 10).astype(np.uint8)[:, :, np.newaxis]
        blended  = base_frame.copy()
        blended  = np.where(mask, cv2.addWeighted(base_frame, 0.4, coloured, 0.6, 0), base_frame)
        return blended.astype(np.uint8)

    def save_png(self, path, base_frame):
        img = self.render(base_frame)
        cv2.imwrite(path, img)
        print(f"[INFO] Heatmap saved: {path}")


